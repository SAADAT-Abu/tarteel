def lookup_region(country: str, city: str) -> tuple[float, float, str] | None:
    """Return (lat, lng, timezone) for a known country+city, or None."""
    country_data = REGIONS.get(country)
    if not country_data:
        return None
    return country_data.get(city)


def get_regions_map() -> dict[str, list[str]]:
    """Return {country: sorted_city_list} for the API response."""
    return {country: sorted(cities.keys()) for country, cities in sorted(REGIONS.items())}
